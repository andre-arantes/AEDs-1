#include <stdio.h>
#include <math.h>
int main(void) {
  int x, y;
  printf("Digite o seu valor de X\n");
  scanf("%d", &x);

  if (x <= 1) {
    y = 1;
  } else if (x > 1 && x <= 2) {
    y = 2;
  } else if (x > 2 && x <= 3) {
    //uso da variável matemática pow como potencia
    y = pow(x, 2);
  } else if (x > 3) {
    y = pow(x, 3);
  }
  printf("O valor de y é igual a: %.2d\n", y);
  return 0;
}
